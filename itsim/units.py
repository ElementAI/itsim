# Time correspondance convention: 1.0 simulated time == 1.0 second
S = 1.0
MIN = 60 * S
H = 60 * MIN
D = 24 * H
W = 7 * D
MS = S * 1.0e-3
US = MS * 1.0e-6
NS = US * 1.0e-9

# Bandwidth units: B == bit, not byte
KbPS = 1024 / 8
MbPS = 1024 * KbPS
GbPS = 1024 * MbPS

# Size units: B == byte
B = 1
KB = 1024 * B
MB = 1024 * KB
GB = 1024 * MB
